export function getLang() {
  if (typeof window === 'undefined') return 'fr';
  return localStorage.getItem('lang') || 'fr';
}

export function t(key: string): string {
  var lang = getLang();
  return translations[lang]?.[key] || translations.fr[key] || key;
}
